public class ReportStatusPair {
    private int status;
    private int count;

    public ReportStatusPair() {
        this.status = -1;
        this.count = -1;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
    
    
}
